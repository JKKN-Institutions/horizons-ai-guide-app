// Utility functions
export const getExamsByCategory = (category: string): EntranceExam[] => {
  return entranceExams.filter((exam) => exam.category === category);
};

export const getExamById = (id: string): EntranceExam | undefined => {
  return entranceExams.find((exam) => exam.id === id);
};

export const getCategoryInfo = (categoryId: string): CategoryInfo | undefined => {
  return examCategories.find((cat) => cat.id === categoryId);
};

export const getExamsByState = (state: IndianState): EntranceExam[] => {
  if (state === 'all') return entranceExams;
  return entranceExams.filter((exam) => exam.state === state || exam.state === 'tamil-nadu');
};

export const getExamsByCategoryAndState = (category: string, state: IndianState): EntranceExam[] => {
  let filtered = entranceExams.filter((exam) => exam.category === category);
  if (state !== 'all') {
    filtered = filtered.filter((exam) => exam.state === state || exam.state === 'tamil-nadu');
  }
  return filtered;
};
